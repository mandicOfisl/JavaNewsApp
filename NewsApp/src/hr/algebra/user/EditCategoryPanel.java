    private void init() {
        try {
            initRepo();
            fillListBox();    
        } catch (Exception e) {
            MessageUtils.showErrorMessage("Error", "Cannot initiate form.");
        }
    }

    private void initRepo() throws Exception {
        repo = RepoFactory.getRepo();
        categoryModel = new DefaultListModel<>();
    }

    private void fillListBox() throws Exception {
        List<Category> categories = repo.getCategories();
        if (categories.size() > 0) {
            categoryModel.clear();
            categories.forEach(cat -> categoryModel.addElement(cat));
            lsCategories.setModel(categoryModel);
        } else {
            throw new Exception();
        }
    }

