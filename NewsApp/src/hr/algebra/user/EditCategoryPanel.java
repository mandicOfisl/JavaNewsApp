    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (lsCategories.getSelectedIndex() > -1) {
            int id = lsCategories.getSelectedValue().getId();
            try {
                repo.deleteCategory(id);
                MessageUtils.showInformationMessage("Success", "Category deleted!");
                fillListBox();
                tfName.setText("");
            } catch (Exception ex) {
                MessageUtils.showErrorMessage("Error", "Cannot delete category");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (!tfName.getText().trim().isEmpty()) {
            try {
                repo.createCategory(tfName.getText().trim());
                MessageUtils.showInformationMessage("Success", "Category added!");
                fillListBox();
                tfName.setText("");
            } catch (Exception ex) {
                MessageUtils.showErrorMessage("Error", "Cannot add category.");
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (lsCategories.getSelectedIndex() > -1 && !tfName.getText().trim().isEmpty()) {
            int id = lsCategories.getSelectedValue().getId();
            try {
                repo.updateCategory(id, tfName.getText().trim());
                MessageUtils.showInformationMessage("Success", "Category updated!");
                fillListBox();
            } catch (Exception ex) {
                MessageUtils.showErrorMessage("Error", "Cannot update category.");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void lsCategoriesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lsCategoriesMouseClicked
        if (lsCategories.getSelectedIndex() > -1) {
            Category cat = lsCategories.getSelectedValue();
            tfName.setText(cat.getName());
        }
    }//GEN-LAST:event_lsCategoriesMouseClicked


